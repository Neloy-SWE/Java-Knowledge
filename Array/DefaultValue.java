// array's default values:


package Array;

public class DefaultValue {
public static void main(String[] args) {
	System.out.println("Integer default value: ");
	int a [] = new int [3];
	for (int i : a) {
		System.out.println(i+" ");
	}
	System.out.println("Dauble default value: ");
	double b [] = new double [3];
	for (double i : b) {
		System.out.println(i+" ");
	}
	System.out.println("Boolen default value: ");
	boolean c [] = new boolean [3];
	for (boolean i : c) {
		System.out.println(i+" ");
	}
	System.out.println("String default value: ");
	String d [] = new String [3];
	for (String i : d) {
		System.out.println(i+" ");
	}
}
}
